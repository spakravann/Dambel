package com.faraa.sohrabpakravan.dambel.Models;

public class MoveImageData {

    int imageOne, imageTwo, imageThree;

    public MoveImageData() {

    }

    public MoveImageData(int _imageOne, int _imageTwo, int _imageThree) {
        this.imageOne = _imageOne;
        this.imageTwo = _imageTwo;
        this.imageThree = _imageThree;

    }

    public int getImageOne() {
        return imageOne;
    }

    public int getImageTwo() {
        return imageTwo;
    }

    public int getImageThree() {
        return imageThree;
    }

}
